// Number - represents numeric values
let num = 10;

// String - represents textual data
let str = "Hello, World!";

// Boolean - represents true/false values
let bool = true;

// Null - represents an intentional absence of any object value
let nul = null;

// Undefined - represents an unintentional absence of any object value
let undef;

// Object - represents a collection of related data or functionality
let obj = { name: "John", age: 30 };

// Array - represents an ordered collection of values
let arr = [1, 2, 3, 4, 5];

// Symbol - represents a unique identifier
let sym = Symbol("foo");

// BigInt - represents integers larger than 2^53 - 1
let bigInt = 9007199254740991n;

// Function - represents a block of code that can be called by name
function add(a, b) {
  return a + b;
}

// Output the values of all the variables to the console
console.log(num, str, bool, nul, undef, obj, arr, sym, bigInt, add);
